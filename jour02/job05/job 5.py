
def calcule(numbre1,operator,numbre2):
    if operator == "+":
     return numbre1+numbre2
    elif operator == "-":
     return numbre1-numbre2
    elif operator =='*':
     return numbre1*numbre2
    elif operator =="/":
     return numbre1/numbre2
    elif operator =="%":
      return numbre1%numbre2
    else:
      return ("operation inconnue") 
    